package com.computergraphic.lab10.geometry;

public class Point2D {
    private int x = 0;
    private int y = 0;

    Point2D(int x, int y) {
        setX(x);
        setY(y);
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
